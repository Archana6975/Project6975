package operators;

public class increment_decrement {

	public static void main(String[] args) {
		int a=40;
		int b=50; 
		 System.out.println("Value of a "+a);
		 System.out.println("Value of b "+b);
		 
		 System.out.println("Value of --a "+(--a));
		 System.out.println("Value of a "+a);
		 System.out.println("Value of b-- "+(b--));
		 System.out.println("Value of b "+b);
		
	}

}
