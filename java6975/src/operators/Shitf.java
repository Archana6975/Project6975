package operators;

public class Shitf {

	public static void main(String[] args) {
		 int number = 2;
		 int b=8;
		   
		    // 2 bit left shift operation 
		    int result = number << 2;
		    System.out.println(result); //01000
		    
		    
		 // 2 bit right shift operation 
		    int res = b >> 2;
		    System.out.println(res); // 10
		    

	}

}
