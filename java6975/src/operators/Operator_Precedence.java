package operators;

public class Operator_Precedence {

	public static void main(String[] args) {
			      int result = 0;
			      result = 5 + 2 * 3 - 1;  // 5+(2*3)-1)
			      System.out.println("5 + 2 * 3 - 1 = " +result); //2*3 = 6, 
			                                                     // 5+6-1 = 10

	}

}
